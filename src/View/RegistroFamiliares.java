    private void limpiaDatos(){
        
        RFC.setText("");
        nombreF.setText("");
        ApellidoF.setText("");
        TelefonoF.setText("");
        Parentesco.setText("");
    }
    
    private void guardaFam() throws SQLException{
        
        ConexionBD con = new ConexionBD();
        Connection conecta;
        
        conecta = con.getConnection();
        PreparedStatement ps;
        
        ps = conecta.prepareStatement("INSERT INTO familiares(RFC_paciente, nombre, apellido1, telefono, parentesco) "
                + "VALUES(?,?,?,?,?)");
        
        ps.setString(1, RFC.getText());
        ps.setString(2, nombreF.getText());
        ps.setString(3, ApellidoF.getText());
        ps.setString(4, TelefonoF.getText());
        ps.setString(5, Parentesco.getText());
        ps.executeUpdate();
        
        ps.close();
        
        JOptionPane.showConfirmDialog(rootPane, "Datos de Familiar Guardados con Exito", "Datos guardados", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE, null);
    }

